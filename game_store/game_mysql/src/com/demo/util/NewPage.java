package com.demo.util;

public class NewPage {
   private int page=1;
   private int limit=10;
   private int size;

public int getPage() {
	return page;
}
public void setPage(int page) {
	this.page = page;
}
public int getLimit() {
	return limit;
}
public void setLimit(int limit) {
	this.limit = limit;
}
public int getSize() {
	return size;
}
public void setSize(int size) {
	this.size = size;
}
   
   
}
